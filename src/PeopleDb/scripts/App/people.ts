class People {

    id: number;
    email: string;
    firstName: string;
    lastName: string;
    roleId: number;

    constructor(id: number, email: string, firstName: string, lastName: string, roleId: number) {
       this.id=id;
       this.email = email;
       this.firstName = firstName;
       this.lastName = lastName;
       this.roleId = roleId;
    }
}

